type FolderRepo struct {
	pool *pgxpool.Pool
}

func NewFolderRepo(pool *pgxpool.Pool) *FolderRepo {
	return &FolderRepo{pool: pool}
}

// EnsureFolderHierarchy creates the folder path /{lib}/{class}/{ownerID}/{attType}/
// and returns the leaf folder ID. Uses ON CONFLICT to be idempotent.
func (r *FolderRepo) EnsureFolderHierarchy(
	ctx context.Context,
	ownerClassLibrary, ownerClassName, ownerID, attachmentType string,
) (uuid.UUID, error) {
	segments := []string{ownerClassLibrary, ownerClassName, ownerID, attachmentType}
	var parentID *uuid.UUID
	var leafID uuid.UUID

	for i, seg := range segments {
		path := "/" + strings.Join(segments[:i+1], "/")

		query := `INSERT INTO folders (id, name, parent_id, path, owner_class_library, owner_class_name, owner_id, is_auto_generated)
			VALUES ($1, $2, $3, $4, $5, $6, $7, TRUE)
			ON CONFLICT (path) DO UPDATE SET updated_at = NOW()
			RETURNING id`

		newID := uuid.New()
		var folderOwnerID string
		if i >= 2 {
			folderOwnerID = ownerID
		}

		var folderID uuid.UUID
		err := r.pool.QueryRow(ctx, query,
			newID, seg, parentID, path,
			ownerClassLibrary, ownerClassName, folderOwnerID,
		).Scan(&folderID)
		if err != nil {
			return uuid.Nil, fmt.Errorf("folder repo: ensure folder %q: %w", path, err)
		}

		parentID = &folderID
		leafID = folderID
	}

	return leafID, nil
}

// LinkDocument links a document to a folder via folder_documents.
func (r *FolderRepo) LinkDocument(ctx context.Context, folderID, documentID uuid.UUID) error {
	query := `INSERT INTO folder_documents (folder_id, document_id)
		VALUES ($1, $2)
		ON CONFLICT (folder_id, document_id) DO NOTHING`

	_, err := r.pool.Exec(ctx, query, folderID, documentID)
	if err != nil {
		return fmt.Errorf("folder repo: link document: %w", err)
	}
	return nil
}

// GetFolderByPath returns a folder by its unique path, or nil if not found.
func (r *FolderRepo) GetFolderByPath(ctx context.Context, path string) (*domain.Folder, error) {
	query := `SELECT id, name, parent_id, path, owner_class_library, owner_class_name, owner_id, is_auto_generated, created_at, updated_at
		FROM folders WHERE path = $1`

	var f domain.Folder
	err := r.pool.QueryRow(ctx, query, path).Scan(
		&f.ID, &f.Name, &f.ParentID, &f.Path,
		&f.OwnerClassLibrary, &f.OwnerClassName, &f.OwnerID,
		&f.IsAutoGenerated, &f.CreatedAt, &f.UpdatedAt,
	)
	if err == pgx.ErrNoRows {
		return nil, nil
	}
	if err != nil {
		return nil, fmt.Errorf("folder repo: get by path: %w", err)
	}
	return &f, nil
}

func (r *FolderRepo) ListRoots(ctx context.Context) ([]domain.Folder, error) {
	query := `SELECT id, name, parent_id, path, owner_class_library, owner_class_name,
		owner_id, is_auto_generated, created_at, updated_at
	FROM folders WHERE parent_id IS NULL ORDER BY name`
	return r.queryFolders(ctx, query)
}

